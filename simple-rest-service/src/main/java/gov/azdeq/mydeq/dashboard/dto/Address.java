public class Address {

  public String location;
  public String street_address1;
  public String street_address2;
  public String city;
  public String state;
  public String zipcode;
  public String country;
  public String latitude;
  public String longitude;

  /**
   * @return the location
   */
  public String getLocation() {
    return location;
  }

  /**
   * @param location the location to set
   */
  public void setLocation(String location) {
    this.location = location;
  }

  /**
   * @return the street_address1
   */
  public String getStreet_address1() {
    return street_address1;
  }

  /**
   * @param street_address1 the street_address1 to set
   */
  public void setStreet_address1(String street_address1) {
    this.street_address1 = street_address1;
  }

  /**
   * @return the street_address2
   */
  public String getStreet_address2() {
    return street_address2;
  }

  /**
   * @param street_address2 the street_address2 to set
   */
  public void setStreet_address2(String street_address2) {
    this.street_address2 = street_address2;
  }

  /**
   * @return the city
   */
  public String getCity() {
    return city;
  }

  /**
   * @param city the city to set
   */
  public void setCity(String city) {
    this.city = city;
  }

  /**
   * @return the state
   */
  public String getState() {
    return state;
  }

  /**
   * @param state the state to set
   */
  public void setState(String state) {
    this.state = state;
  }

  /**
   * @return the zipcode
   */
  public String getZipcode() {
    return zipcode;
  }

  /**
   * @param zipcode the zipcode to set
   */
  public void setZipcode(String zipcode) {
    this.zipcode = zipcode;
  }

  /**
   * @return the country
   */
  public String getCountry() {
    return country;
  }

  /**
   * @param country the country to set
   */
  public void setCountry(String country) {
    this.country = country;
  }

  /**
   * @return the latitude
   */
  public String getLatitude() {
    return latitude;
  }

  /**
   * @param latitude the latitude to set
   */
  public void setLatitude(String latitude) {
    this.latitude = latitude;
  }

  /**
   * @return the longitude
   */
  public String getLongitude() {
    return longitude;
  }

  /**
   * @param longitude the longitude to set
   */
  public void setLongitude(String longitude) {
    this.longitude = longitude;
  }

}
